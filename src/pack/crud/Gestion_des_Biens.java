    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();

        if (row == -1) {
            return;
        }

        int idBien = (int) jTable1.getValueAt(row, 0);
        txtNum.setText(String.valueOf(idBien));

        String type = jTable1.getValueAt(row, 1).toString();
        comboType.setSelectedItem(type);

        txtVille.setText(jTable1.getValueAt(row, 2).toString());

        txtSurface.setText(jTable1.getValueAt(row, 3).toString());

        txtPrix.setText(jTable1.getValueAt(row, 4).toString());

        int idProprio = (int) jTable1.getValueAt(row, 5);

        for (int i = 0; i < comboProprio.getItemCount(); i++) {
            Proprietaire p = (Proprietaire) comboProprio.getItemAt(i);
            if (p.getIdProprietaire() == idProprio) {
                comboProprio.setSelectedIndex(i);
                break;
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void viderChamps() {
        txtNum.setText("");
        txtVille.setText("");
        txtSurface.setText("");
        txtPrix.setText("");
        comboType.setSelectedIndex(0);
        comboProprio.setSelectedIndex(0);

    }

    private void chargerTable(List<Bien> list) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // vider la table

        for (Bien b : list) {
            model.addRow(new Object[]{
                b.getIdBien(),
                b.getType(),
                b.getVille(),
                b.getSurface(),
                b.getPrixMensuel(),
                b.getIdProprietaire()
            });
        }
    }

    private void chargerComboRVille() {
        comboRVille.removeAllItems();
        comboRVille.addItem("Toutes");

        for (String ville : dao.findVilles()) {
            comboRVille.addItem(ville);
        }
    }

    private void chargerComboRType() {
        ComboRType.removeAllItems();
        ComboRType.addItem("Tous"); 

        for (String type : dao.findTypes()) {
            ComboRType.addItem(type);
        }
    }

    private void chargerComboType() {
        comboType.removeAllItems();

        for (String type : dao.findTypes()) {
            comboType.addItem(type);
        }
    }

    private void chargerComboProprietaire() {
        comboProprio.removeAllItems();

        for (Proprietaire p : proprietaireDao.findAll()) {
            comboProprio.addItem(p); 
        }
    }

