    private void btnRechercheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRechercheActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtCherche.getText());
        Bien b = dao.findById(id);

        if (b != null) {
            txtNum.setText(String.valueOf(b.getIdBien()));
            comboType.setSelectedItem(b.getType());
            txtVille.setText(b.getVille());
            txtSurface.setText(String.valueOf(b.getSurface()));
            txtPrix.setText(String.valueOf(b.getPrixMensuel()));

            // ⭐ Sélection du propriétaire
            for (int i = 0; i < comboProprio.getItemCount(); i++) {
                Proprietaire p = (Proprietaire) comboProprio.getItemAt(i);
                if (p.getIdProprietaire() == b.getIdProprietaire()) {
                    comboProprio.setSelectedIndex(i);
                    break;
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Bien introuvable");
        }
    }//GEN-LAST:event_btnRechercheActionPerformed

    private void comboRVilleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboRVilleActionPerformed
        // TODO add your handling code here:
        if (comboRVille.getSelectedItem() == null) {
            return;
        }

        String ville = comboRVille.getSelectedItem().toString();

        if (ville.equals("Toutes")) {
            chargerTable(dao.findAll());
        } else {
            chargerTable(dao.findByVille(ville));
        }
    }//GEN-LAST:event_comboRVilleActionPerformed

