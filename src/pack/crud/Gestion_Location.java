    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        chargementDepuisTable = true;

        int row = jTable1.getSelectedRow();

        txtNumL.setText(model.getValueAt(row, 0).toString());
        txtLoyer.setText(model.getValueAt(row, 4).toString());

        jDateChooserDebut.setDate(
                new java.util.Date(((Date) model.getValueAt(row, 2)).getTime())
        );
        jDateChooserFin.setDate(
                new java.util.Date(((Date) model.getValueAt(row, 3)).getTime())
        );

        int bienId = (int) model.getValueAt(row, 1);
        for (int i = 0; i < jComboBien.getItemCount(); i++) {
            Bien b = (Bien) jComboBien.getItemAt(i);
            if (b.getIdBien() == bienId) {
                jComboBien.setSelectedIndex(i);
                break;
            }
        }

        chargementDepuisTable = false;
    }//GEN-LAST:event_jTable1MouseClicked

    private void jDateChooserFinPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jDateChooserFinPropertyChange
        // TODO add your handling code here:
        if (!"date".equals(evt.getPropertyName())) {
            return;
        }

        if (chargementDepuisTable) {
            return;
        }

        if (jDateChooserDebut.getDate() == null
                || jDateChooserFin.getDate() == null
                || jComboBien.getSelectedItem() == null) {
            return;
        }

        Date debut = new Date(jDateChooserDebut.getDate().getTime());
        Date fin = new Date(jDateChooserFin.getDate().getTime());

        if (!debut.before(fin)) {
            txtLoyer.setText("");
            JOptionPane.showMessageDialog(this,
                    "La date de fin doit être après la date de début");
            return;
        }

        Bien b = (Bien) jComboBien.getSelectedItem();
        double total = locationDao.calculLoyer(debut, fin, b.getPrixMensuel());

        txtLoyer.setText(String.valueOf(total));
    }//GEN-LAST:event_jDateChooserFinPropertyChange

    private void vider() {
        txtNumL.setText("");
        txtLoyer.setText("");
        jDateChooserDebut.setDate(null);
        jDateChooserFin.setDate(null);
        jComboBien.setSelectedIndex(-1);
        jTable1.clearSelection();
    }

    private void chargerComboBien() {
        jComboBien.removeAllItems();
        for (Bien b : bienDao.findAll()) {
            jComboBien.addItem(b);
        }
    }

    private void chargerTable(List<Location> list) {
        model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (Location l : list) {
            model.addRow(new Object[]{
                l.getIdLocation(),
                l.getIdBien(),
                l.getDateDebut(),
                l.getDateFin(),
                l.getLoyer()
            });
        }
    }

