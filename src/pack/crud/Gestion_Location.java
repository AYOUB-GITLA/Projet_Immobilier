    private void btnchercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnchercherActionPerformed
        // TODO add your handling code here:
        if (jDateChooser1.getDate() == null || jDateChooser2.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Veuillez saisir les deux dates");
            return;
        }

        Date debut = new Date(jDateChooser1.getDate().getTime());
        Date fin = new Date(jDateChooser2.getDate().getTime());

        if (!debut.before(fin)) {
            JOptionPane.showMessageDialog(this, "Date début doit être avant date fin");
            return;
        }

        chargerTable(locationDao.rechercheParDate(debut, fin));
    }//GEN-LAST:event_btnchercherActionPerformed

    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        // TODO add your handling code here:
        if (jComboBien.getSelectedItem() == null || jDateChooserDebut.getDate() == null || jDateChooserFin.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs");
            return;
        }

        Date debut = new Date(jDateChooserDebut.getDate().getTime());
        Date fin = new Date(jDateChooserFin.getDate().getTime());

        if (!debut.before(fin)) {
            JOptionPane.showMessageDialog(this, "Date fin doit être après date début");
            return;
        }

        Bien b = (Bien) jComboBien.getSelectedItem();
        double total = locationDao.calculLoyer(debut, fin, b.getPrixMensuel());

        Location l = new Location();
        l.setIdBien(b.getIdBien());
        l.setDateDebut(debut);
        l.setDateFin(fin);
        l.setLoyer(total);

        if (locationDao.create(l)) {
            JOptionPane.showMessageDialog(this, "Location ajoutée");
            chargerTable(locationDao.findAll());
            if (monParent != null) {
                monParent.chargerTable(); 
                monParent.chargerGraphes();
            }
            vider();
        }
    }//GEN-LAST:event_btnAjouterActionPerformed

    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
        // TODO add your handling code here:
        if (txtNumL.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sélectionnez une location");
            return;
        }

        int rep = JOptionPane.showConfirmDialog(this, "Supprimer ?");
        if (rep == JOptionPane.YES_OPTION) {
            Location l = new Location();
            l.setIdLocation(Integer.parseInt(txtNumL.getText()));
            locationDao.delete(l);
            chargerTable(locationDao.findAll());
            if (monParent != null) {
                monParent.chargerTable(); 
                monParent.chargerGraphes();
            }
            vider();
        }
    }//GEN-LAST:event_btnSupprimerActionPerformed

    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        // TODO add your handling code here:
        if (txtNumL.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sélectionnez une location");
            return;
        }

        Date debut = new Date(jDateChooserDebut.getDate().getTime());
        Date fin = new Date(jDateChooserFin.getDate().getTime());

        if (!debut.before(fin)) {
            JOptionPane.showMessageDialog(this, "Dates invalides");
            return;
        }

        Bien b = (Bien) jComboBien.getSelectedItem();
        double total = locationDao.calculLoyer(debut, fin, b.getPrixMensuel());

        Location l = new Location();
        l.setIdLocation(Integer.parseInt(txtNumL.getText()));
        l.setIdBien(b.getIdBien());
        l.setDateDebut(debut);
        l.setDateFin(fin);
        l.setLoyer(total);

        if (locationDao.update(l)) {
            JOptionPane.showMessageDialog(this, "Location modifiée");
            chargerTable(locationDao.findAll());
            if (monParent != null) {
                monParent.chargerTable(); 
                monParent.chargerGraphes();
            }
            vider();
        }

    }//GEN-LAST:event_btnModifierActionPerformed

